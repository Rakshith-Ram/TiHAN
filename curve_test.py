# Calculating and returning the angle formed by the line segments formed by the considered waypoints

def calculate_angle(point1, point2, point3):
 
    dx1 = point1[0] - point2[0]
    dy1 = point1[1] - point2[1]
    dx2 = point3[0] - point2[0]
    dy2 = point3[1] - point2[1]
    
    if (dx2 == 0):
    	t1 = 90
    else :
    	t1 = np.arctan(dy2/dx2) * (180 / math.pi)
    #print("t1 = ", t1)
    
    if (dx1 == 0):
    	t2 = 90
    else : 
    	t2 = np.arctan(dy1/dx1) * (180 / math.pi)
    #print("t2 = ", t2)
    
    angle_degrees = t1-t2
    return angle_degrees


